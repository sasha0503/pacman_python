class Ghost:
    def __init__(self, x_coord, y_coord, target, speed, img, direct, dead, box, id):
        self.x_pos = x_coord
        self.y_pos = y_coord
        self.center_x = self.x_pos + 22
        self.center_y = self.y_pos + 22
        self.target = target
        self.speed = speed
        self.img = img
        self.direction = direct
        self.dead = dead
        self.in_box = box
        self.id = id
        self.turns, self.in_box = self.check_collisions()
        self.rect = self.draw()

    def draw(self):
        if (not powerup and not self.dead) or (eaten_ghost[self.id] and powerup and not self.dead):
            screen.blit(self.img, (self.x_pos, self.y_pos))
        elif powerup and not self.dead and not eaten_ghost[self.id]:
            screen.blit(spooked_img, (self.x_pos, self.y_pos))
        else:
            screen.blit(dead_img, (self.x_pos, self.y_pos))
        ghost_rect = pygame.rect.Rect((self.center_x - 18, self.center_y - 18), (36, 36))
        return ghost_rect

    def check_collisions(self):
        # R, L, U, D
        self.turns = [False, False, False, False]
        if 0 < self.center_x // 30 < 29:
            if level[(self.center_y - FUDGE_NUMBER) // tile_height][self.center_x // tile_width] == 9:
                self.turns[2] = True
            if level[self.center_y // tile_height][(self.center_x - FUDGE_NUMBER) // tile_width] < 3 \
                    or (level[self.center_y // tile_height][(self.center_x - FUDGE_NUMBER) // tile_width] == 9 and (
                    self.in_box or self.dead)):
                self.turns[1] = True
            if level[self.center_y // tile_height][(self.center_x + FUDGE_NUMBER) // tile_width] < 3 \
                    or (level[self.center_y // tile_height][(self.center_x + FUDGE_NUMBER) // tile_width] == 9 and (
                    self.in_box or self.dead)):
                self.turns[0] = True
            if level[(self.center_y + FUDGE_NUMBER) // tile_height][self.center_x // tile_width] < 3 \
                    or (level[(self.center_y + FUDGE_NUMBER) // tile_height][self.center_x // tile_width] == 9 and (
                    self.in_box or self.dead)):
                self.turns[3] = True
            if level[(self.center_y - FUDGE_NUMBER) // tile_height][self.center_x // tile_width] < 3 \
                    or (level[(self.center_y - FUDGE_NUMBER) // tile_height][self.center_x // tile_width] == 9 and (
                    self.in_box or self.dead)):
                self.turns[2] = True
        else:
            self.turns[0] = True
            self.turns[1] = True
        if 350 < self.x_pos < 550 and 370 < self.y_pos < 480:
            self.in_box = True
        else:
            self.in_box = False
        return self.turns, self.in_box

